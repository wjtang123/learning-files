package selflearning.jvmandgc;

/**
 * JVM 内存结构详解
 *
 * JVM 运行时数据区分为五个部分，面试必须能手画出来：
 *
 *   线程共享（所有线程共用，GC 主要在这里工作）：
 *     1. 堆（Heap）         ← 最重要，存放几乎所有对象
 *     2. 方法区/元空间      ← 存放类信息、常量、静态变量
 *
 *   线程私有（每个线程独有，随线程创建/销毁）：
 *     3. 虚拟机栈           ← 存放方法调用的栈帧
 *     4. 本地方法栈         ← Native 方法专用
 *     5. 程序计数器         ← 记录当前线程执行到哪行字节码
 */
public class JVM1_Memory {

    // ============================================================
    // 一、堆（Heap）—— 最核心的区域
    // ============================================================
    /*
     * 堆的结构（JDK8，使用分代收集器）：
     *
     * ┌─────────────────────────────────────────────────────┐
     * │                      堆（Heap）                      │
     * │                                                     │
     * │  ┌──────────────────────────┐  ┌─────────────────┐  │
     * │  │       新生代（Young）      │  │   老年代（Old）   │  │
     * │  │ ┌────────┬──────┬──────┐ │  │                 │  │
     * │  │ │ Eden   │ S0   │ S1   │ │  │  长期存活的对象    │  │
     * │  │ │  80%   │  10% │  10% │ │  │                 │  │
     * │  │ └────────┴──────┴──────┘ │  │                 │  │
     * │  └──────────────────────────┘  └─────────────────┘  │
     * │                                                     │
     * └─────────────────────────────────────────────────────┘
     *
     * 对象生命周期：
     *   新对象 → Eden 区
     *   Eden 满 → Minor GC → 存活对象移到 S0/S1（年龄+1）
     *   年龄到达阈值（默认15）→ 晋升老年代
     *   老年代满 → Full GC（整堆回收，STW 时间长）
     *
     * JDK8 默认堆大小：物理内存的 1/64（初始）到 1/4（最大）
     *   -Xms256m  初始堆大小
     *   -Xmx512m  最大堆大小（建议初始=最大，避免动态扩容）
     *   -Xmn128m  新生代大小（新生代=1/3堆，老年代=2/3堆 是常见比例）
     */

    // ============================================================
    // 二、虚拟机栈（VM Stack）—— 方法调用的载体
    // ============================================================
    /*
     * 每个线程有一个虚拟机栈。
     * 每调用一个方法，就压入一个"栈帧"；方法返回，栈帧弹出。
     *
     * 栈帧包含：
     *   局部变量表  ← 方法的局部变量（基本类型存值，引用类型存地址）
     *   操作数栈    ← 方法执行时的计算中间结果
     *   动态链接    ← 指向运行时常量池中该方法的引用
     *   返回地址    ← 方法执行完后回到哪里继续执行
     *
     * StackOverflowError：递归太深，栈帧太多，栈空间耗尽
     * -Xss256k：每个线程栈的大小（默认 512KB~1MB）
     */

    // 演示 StackOverflowError
    static int depth = 0;
    static void infiniteRecursion() {
        depth++;
        infiniteRecursion(); // 递归调用，每次都压入新栈帧
    }

    // ============================================================
    // 三、方法区 / 元空间（Metaspace）
    // ============================================================
    /*
     * JDK7 及之前：方法区在 JVM 内存里（PermGen 永久代）
     *   -XX:PermSize=64m  -XX:MaxPermSize=256m
     *   问题：大小固定，频繁加载类会导致 java.lang.OutOfMemoryError: PermGen space
     *
     * JDK8+：方法区改为元空间（Metaspace），移到本地内存（OS 内存）
     *   -XX:MetaspaceSize=64m       初始大小
     *   -XX:MaxMetaspaceSize=256m   最大大小（不设则无限增长）
     *   优势：不受 JVM 堆限制，用多少占多少，OOM 概率大幅降低
     *
     * 存储内容：
     *   - 类的结构信息（字段、方法、接口）
     *   - 运行时常量池（字面量、符号引用）
     *   - 静态变量（JDK8 移到堆中）
     *   - JIT 编译后的代码缓存
     */

    // ============================================================
    // 四、对象在内存中的布局
    // ============================================================
    /*
     * 一个 Java 对象在堆里的结构（64位JVM，开启指针压缩）：
     *
     * ┌───────────────┬──────────────────────────────────────┐
     * │   对象头       │  Mark Word（8字节）                   │
     * │（12字节）      │  存锁信息、GC年龄、hashCode           │
     * │               ├──────────────────────────────────────┤
     * │               │  Klass Pointer（4字节，压缩后）        │
     * │               │  指向方法区的类型信息                  │
     * ├───────────────┼──────────────────────────────────────┤
     * │   实例数据     │  各字段的值                           │
     * │               │  引用类型占4字节（压缩后）              │
     * ├───────────────┼──────────────────────────────────────┤
     * │   对齐填充     │  补齐到8字节的倍数（可能为0）           │
     * └───────────────┴──────────────────────────────────────┘
     *
     * 最小对象大小：16字节（12字节对象头 + 4字节对齐）
     */

    // ============================================================
    // 五、对象创建的完整过程
    // ============================================================
    /*
     * User user = new User("Alice", 25); 这一行背后发生了什么？
     *
     * 步骤一：检查类是否已加载
     *   JVM 在方法区查找 User 类，没找到则触发类加载
     *
     * 步骤二：分配内存
     *   在堆的 Eden 区分配内存（大小在类加载时已确定）
     *   两种分配方式：
     *   - 指针碰撞（内存规整时，Serial/ParNew 使用）：移动指针
     *   - 空闲列表（内存碎片化时，CMS 使用）：从列表找空闲块
     *   线程安全：TLAB（Thread Local Allocation Buffer）
     *   每个线程在 Eden 里预分配一块私有区域，避免并发冲突
     *
     * 步骤三：初始化零值
     *   字段初始化为默认值（0/false/null），这就是为什么不赋值也能用
     *
     * 步骤四：设置对象头
     *   写入类型指针、GC年龄、锁状态等信息
     *
     * 步骤五：执行 <init> 方法
     *   按代码顺序执行：父类构造器 → 字段初始化 → 构造器方法体
     */

    static class User {
        String name;
        int age;

        User(String name, int age) {
            this.name = name;
            this.age  = age;
        }
    }

    // ============================================================
    // 六、堆外内存（Direct Memory）
    // ============================================================
    /*
     * NIO 的 ByteBuffer.allocateDirect() 分配的内存不在 JVM 堆里，
     * 直接使用操作系统内存，称为堆外内存（Direct Memory）。
     *
     * 优点：IO 操作时不需要在堆内外之间复制数据，性能高
     * 缺点：不受 GC 管理，需要手动或通过 Cleaner 释放
     *       设置上限：-XX:MaxDirectMemorySize=256m
     *
     * Netty 大量使用堆外内存提高网络IO性能
     */

    // ============================================================
    // 七、常见 OOM 类型和原因
    // ============================================================
    /*
     * java.lang.OutOfMemoryError: Java heap space
     *   堆内存不足，最常见的 OOM
     *   原因：对象太多、内存泄漏（对象被引用无法回收）
     *   排查：-XX:+HeapDumpOnOutOfMemoryError 生成 dump 文件
     *
     * java.lang.OutOfMemoryError: Metaspace
     *   元空间不足（JDK8+）
     *   原因：动态生成大量类（如使用 CGLIB）
     *   解决：-XX:MaxMetaspaceSize=256m
     *
     * java.lang.OutOfMemoryError: GC overhead limit exceeded
     *   GC 时间占比超过 98%，但回收内存不足 2%
     *   说明内存严重不足，几乎所有时间都在 GC
     *
     * java.lang.OutOfMemoryError: Direct buffer memory
     *   堆外内存不足
     *
     * java.lang.StackOverflowError
     *   不是 OOM，是栈溢出（递归太深或栈大小太小）
     */

    // ============================================================
    // main：演示各种内存区域的实际表现
    // ============================================================
    public static void main(String[] args) {

        System.out.println("=== JVM 内存信息 ===");
        Runtime rt = Runtime.getRuntime();
        System.out.println("最大堆：" + rt.maxMemory() / 1024 / 1024 + " MB");
        System.out.println("当前堆：" + rt.totalMemory() / 1024 / 1024 + " MB");
        System.out.println("空闲堆：" + rt.freeMemory() / 1024 / 1024 + " MB");
        System.out.println("CPU核数：" + rt.availableProcessors());

        System.out.println("\n=== 栈帧演示：递归调用 ===");
        try {
            infiniteRecursion();
        } catch (StackOverflowError e) {
            System.out.println("StackOverflowError！递归深度：" + depth);
            // 说明每次递归都压了一个栈帧，最终把栈撑满
        }

        System.out.println("\n=== 对象在堆中创建 ===");
        // 每个 new User() 都在堆的 Eden 区分配内存
        User u1 = new User("Alice", 25);
        User u2 = new User("Bob", 30);
        System.out.println("u1 在堆中，u1 引用在栈帧的局部变量表里");
        System.out.println("u1.name=" + u1.name + ", u2.name=" + u2.name);

        System.out.println("\n=== 字符串常量池（方法区/堆）===");
        // 字符串字面量存在字符串常量池（JDK7+ 在堆中）
        String s1 = "hello";   // 常量池
        String s2 = "hello";   // 同一个对象
        String s3 = new String("hello"); // 堆中新建对象

        System.out.println("s1==s2（常量池）：" + (s1 == s2)); // true
        System.out.println("s1==s3（new）：" + (s1 == s3));    // false
        System.out.println("s1.equals(s3)：" + s1.equals(s3)); // true

        // intern()：把字符串放入常量池，或返回已有的常量池引用
        String s4 = s3.intern();
        System.out.println("s1==s4（intern）：" + (s1 == s4)); // true

        /*
         * 面试必答：
         *
         * Q：Java 对象一定在堆上分配吗？
         * A：不一定。JIT 的逃逸分析（Escape Analysis）发现对象不会逃出方法范围时，
         *    会进行"栈上分配"或"标量替换"，直接在栈帧里存储，减少 GC 压力。
         *    -XX:+DoEscapeAnalysis（JDK8 默认开启）
         *
         * Q：方法区和堆的区别？
         * A：堆存放对象实例；方法区存放类的结构信息（字段、方法描述、常量池）。
         *    JDK8 把方法区的实现从 PermGen 换成了 Metaspace，迁移到本地内存。
         *
         * Q：程序计数器为什么是线程私有的？
         * A：线程切换后需要知道从哪里继续执行。每个线程都有自己的执行位置，
         *    所以必须是私有的。它是 JVM 中唯一不会发生 OOM 的区域。
         */
    }
}
